export function getDramaStats(): { name: string; count: number; platform: string }[] {
  const dramaMap = new Map<string, { count: number; platform: string }>();

  characters.forEach(char => {
    if (!dramaMap.has(char.drama)) {
      dramaMap.set(char.drama, { count: 1, platform: char.platform });
    } else {
      const data = dramaMap.get(char.drama)!;
      data.count++;
    }
  });

  return Array.from(dramaMap.entries())
    .map(([name, data]) => ({ name, ...data }))
    .sort((a, b) => b.count - a.count);
}

